package com.andrein.expensestracker.Models;
public class User {
    private int id;
    private String username;
    private String firstName;
    private String lastName;

    public int getId() {
        return id;
    }

    public String getUsername() {
        return username;
    }

    public String getFirstName() {
        return firstName;
    }

    public String getLastName() {
        return lastName;
    }
}